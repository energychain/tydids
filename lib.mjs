export default function(privateKey , provider) {
    const isObject = function(payload) { if(Object.prototype.toString.call(payload).indexOf('Object') !== -1) return true; else return false; }
    
    let wallet = null;

    if(isObject(privateKey)) {
        wallet = privateKey;
    } else {

        if((typeof provider == 'undefined')||(provider == null)) {
            provider = new ethers.providers.JsonRpcProvider("https://rpc.stromkonto.net/");
        }
        
        
        if((typeof privateKey == 'undefined') || (privateKey == null)) {
            wallet = ethers.Wallet.createRandom();
            privateKey = wallet.privateKey;
        } 
        wallet =  new ethers.Wallet( privateKey , provider );
    
    }

    // Overwrite and Extend standard ethers JS implementation;
    wallet.tydids = {};

    wallet.tydids.signMessage = async function(payload) {
        if(isObject(payload)) {
            payload = JSON.stringify(payload);
        }
        return await wallet.signMessage(payload);
    }

    wallet.tydids.verifyMessage = function(payload,signature) {
        if(isObject(payload)) {
            if((typeof signature == 'undefined')||(signature == null)) {
                signature = ""+payload.signature;
                delete payload.signature;
            }
            payload = JSON.stringify(payload);
        }
        return ethers.utils.verifyMessage(payload,signature);
    } 
    wallet.tydids.issuer = wallet.tydids.verifyMessage;

    wallet.tydids.hashMessage = function(payload) {
        if(isObject(payload)) {
            payload = JSON.stringify(payload);
        }
        return ethers.utils.hashMessage(payload); 
    }

    wallet.tydids.deployment = {"ABIS":ABIS};
    wallet.tydids.contracts = {};
    wallet.tydids.contracts.GHGTOKEN = new ethers.Contract('0x5dEd359ef9279DC18398973B885401951315831e', wallet.tydids.deployment.ABIS.GHGTOKEN, wallet);
    wallet.tydids.contracts.GHGSAVINGS = new ethers.Contract('0xd17966949655Dbb4A216B7e8e09CDC7358F09abf', wallet.tydids.deployment.ABIS.GHG20, wallet);
    wallet.tydids.contracts.GHGEMISSIONS = new ethers.Contract('0xa2D1a2Df3b13749238B3546C42a92CeF12223439', wallet.tydids.deployment.ABIS.GHG20, wallet);
    wallet.tydids.contracts.GHGSURPLUS = new ethers.Contract('0x87FB78C89C12d41eF8F17978043a79F082e2Cd75', wallet.tydids.deployment.ABIS.GHG20, wallet);
    wallet.tydids.contracts.GHGCERTIFICATES = new ethers.Contract('0x1081548b2485F8436B0fDbDddcAaFB0C482072ff', wallet.tydids.deployment.ABIS.GHG721, wallet);
 
    return wallet;
}
